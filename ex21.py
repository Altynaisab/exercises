def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

Adding = add(5,2)
Subtracting = subtract(7, 2)
Multiplying = multiply(3, 4)
Dividing = divide(3, 6)

print("function Adding is ", Adding)
print("function Subtracting is ", Subtracting)
print("function Multiplying is ", Multiplying)
print("function Dividing is ", Dividing)
